# Solicite um número ao usuário e teste se ele é par ou impar
# Exiba o resultado 

print("Inicio do programa \n")
print("Bem vindo ao nosso programa , para descobrir se um número é par ou impar.\n")

valor = int(input("Digite um número \n"))

if (valor % 2) == 0:
    print(f"O valor inserido foi {valor} , logo ele é PAR \n")

else:

    print(f"O valor inserido foi {valor} , logo ele é IMPAR \n") 


